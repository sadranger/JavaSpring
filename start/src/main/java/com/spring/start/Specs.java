package com.spring.start;

public class Specs {
	private String name;
	private String range;
	private String speed;
	private String indicator;
	private String wanPorts;
	private String lanPorts;
	private String antenna;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRange() {
		return range;
	}
	public void setRange(String range) {
		this.range = range;
	}
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public String getIndicator() {
		return indicator;
	}
	public void setIndicator(String indicator) {
		this.indicator = indicator;
	}
	public String getWanPorts() {
		return wanPorts;
	}
	public void setWanPorts(String wanPorts) {
		this.wanPorts = wanPorts;
	}
	public String getLanPorts() {
		return lanPorts;
	}
	public void setLanPorts(String lanPorts) {
		this.lanPorts = lanPorts;
	}
	public String getAntenna() {
		return antenna;
	}
	public void setAntenna(String antenna) {
		this.antenna = antenna;
	}
	

}
